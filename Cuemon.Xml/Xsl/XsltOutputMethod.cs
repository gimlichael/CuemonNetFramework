namespace Cuemon.Xml.Xsl
{
	/// <summary>
	/// Defines the output format of an XSLT.
	/// </summary>
	public enum XsltOutputMethod
	{
		/// <summary>
		/// Specifies XML as the output format.
		/// </summary>
		Xml,
		/// <summary>
		/// Specifies HTML as the output format.
		/// </summary>
		Html,
		/// <summary>
		/// Specifies TEXT as the output format.
		/// </summary>
		Text
	}
}